    private void MostrarCamionesEnTabla() throws SQLException {
        
        
        
        String[] columnas = {"ID CAMION", "Patente", "Marca", "Anio", "Kilometraje", "Conductor"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);
        
        DAOCamion daoCamion = new DAOCamion();
        ArrayList<Camion> camiones = daoCamion.getListaCamiones();

        for (int i = 0; i < camiones.size(); i++) {
            Camion c = camiones.get(i);
            Object[] fila = {
                c.getIdCamion(),
                c.getPatenteCamion(),
                c.getModelo(),
                c.getAnio(),
                c.getKilometraje(),
                c.getIdConductor()
            };
            modelo.addRow(fila);
        }
        tblCamiones.setModel(modelo);
    }

