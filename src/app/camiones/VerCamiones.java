    public void cargarTabla(String filtro) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Patente");
        model.addColumn("Marca");
        model.addColumn("Modelo");
        model.addColumn("Año");
        model.addColumn("Kilometraje");
        model.addColumn("Conductor");

        try {
            DAOCamion dao = new DAOCamion();
            List<Camion> lista = dao.getCamiones(filtro);

            if (lista.isEmpty()) {
                tblTablaCamiones.setModel(model);
                JOptionPane.showMessageDialog(this, "No se encontraron camiones.", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            for (Camion c : lista) {
                Object[] fila = {
                    c.getIdCamion(),
                    c.getPatenteCamion(),
                    c.getMarca(),
                    c.getModelo(),
                    c.getAnio(),
                    c.getKilometraje(),
                };
                model.addRow(fila);
            }

            tblTablaCamiones.setModel(model); //
            tblTablaCamiones.setAutoCreateRowSorter(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar camiones: " + ex.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }

